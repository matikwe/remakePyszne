package com.example.remakepyszne.util;

public class Types {
    private int typeid;
    private String type;

    public Types(int typeid, String type) {
        this.typeid = typeid;
        this.type = type;
    }

    public int getTypeid() {
        return typeid;
    }

    public String getType() {
        return type;
    }
}
